/**
 * Get a date which represents the time at the beginning of the hour in UTC time. 
 * 
 * @param {Date} date The date to get the beginning of the hour hour of.
 * @returns {Date} The beginning of the hour of {@code date}.
 */
export function dateToUTCHour(date: Date): Date {
  return new Date(Date.UTC(date.getUTCFullYear(), date.getUTCMonth(), date.getUTCDate(), date.getUTCHours(), 0, 0, 0));
}